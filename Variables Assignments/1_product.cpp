// Q1 - Take 2 integer values in two variables x and y and print their product.

#include <iostream>
using namespace std;
int main()
{
    int x, y;
    cout << "Enter the 1st number :\t";
    cin >> x;
    cout << "Enter the 2nd number :\t";
    cin >> y;
    cout << "The product of the two numbers are :\t" << x * y;
}
